# Tupla: Definida por chaves (). Exemplo: tupla = {1, 2, 3}.

# conjuntos não têm uma ordem específica, como uma lista.


conjunto = {"Tomie", "Ayako"}
conjunto1 = {"Miki", "Matsuri", "Haruka"}
conjunto.add("Haruka")
# conjunto.pop(0) # .pop é usado para remover um dado do conjunto ou lista. Se você não colocar nenhum parâmetro, o .pop vai remover o primeiro dado do conjunto. 


print(conjunto)
print(conjunto.difference(conjunto1)) # .difference faz a comparação entre as variáveis, e retorna qual é a diferença dos itens de uma cada. No caso dos 2 conjuntos, nenhum deles tem um dado igual, então vai mostra todos os dados como diferentes.  



